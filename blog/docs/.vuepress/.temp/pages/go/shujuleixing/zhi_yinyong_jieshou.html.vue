<template><div><p>方法的接收者:</p>
<blockquote>
<ul>
<li>值类型，既可以调用值接收者的方法，也可以调用指针接收者的方法；</li>
<li>指针类型，既可以调用指针接收者的方法，也可以调用值接收者的方法。</li>
</ul>
</blockquote>
<p>但是接口的实现，值类型接收者和指针类型接收者不一样：</p>
<blockquote>
<ul>
<li>以值类型接收者实现接口，类型本身和该类型的指针类型，都实现了该接口；</li>
<li>以指针类型接收者实现接口，只有对应的指针类型才被认为实现了接口。</li>
</ul>
</blockquote>
<p>通常我们使用指针作为方法的接收者的理由：</p>
<blockquote>
<ul>
<li>使用指针方法能够修改接收者指向的值。可以避免在每次调用方法时复制该值，在值的类型为大型结构体时，这样做会更加高效。</li>
</ul>
</blockquote>
</div></template>


